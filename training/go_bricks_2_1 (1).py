import numpy as np
import pandas as pd
from sklearn.preprocessing import MinMaxScaler
from sklearn.model_selection import train_test_split
from sklearn.metrics import mean_absolute_error
import tensorflow as tf
from tensorflow.keras.models import Sequential, Model # Added Model
from tensorflow.keras.layers import LSTM, Dense, Dropout, Input, RepeatVector, TimeDistributed # Added Input, RepeatVector, TimeDistributed
import matplotlib.pyplot as plt
import joblib

# ─── DATA LOAD ───────────────────────────────────────────
df = pd.read_csv("weather_labeled.csv").dropna()

FEATURES = ["temperature", "humidity", "wind_speed",
            "cloud_cover", "precipitation", "pressure", "solar_rad"]

TARGETS = ["storm_label", "solar_drop_pct", "wind_drop_pct"]

# ─── SCALING ─────────────────────────────────────────────
scaler_X = MinMaxScaler()
scaler_y = MinMaxScaler()

X_scaled = scaler_X.fit_transform(df[FEATURES])
y_scaled = scaler_y.fit_transform(df[TARGETS])

# ─── SEQUENCE BANANA (LSTM ke liye zaroori) ─────────
SEQ_LEN      = 24   # same as before — 24h input history
PRED_HORIZON = 6    # NEW — predict next 6 hours

def make_multistep_sequences(X, y, seq_len, horizon):
    Xs, ys = [], []
    for i in range(len(X) - seq_len - horizon + 1):
        Xs.append(X[i : i + seq_len])
        ys.append(y[i + seq_len : i + seq_len + horizon])
    return np.array(Xs), np.array(ys)

X_seq, y_seq = make_multistep_sequences(X_scaled, y_scaled, SEQ_LEN, PRED_HORIZON)
print(f"X shape: {X_seq.shape}  →  (samples, {SEQ_LEN}h input, {len(FEATURES)} features)")
print(f"y shape: {y_seq.shape}  →  (samples, {PRED_HORIZON}h forecast, {len(TARGETS)} targets)")

# ─── TRAIN/TEST SPLIT (time order — no shuffle) ──────────
split        = int(len(X_seq) * 0.80)
X_train, X_test = X_seq[:split], X_seq[split:]
y_train, y_test = y_seq[:split], y_seq[split:]
print(f"Train: {X_train.shape[0]} samples  |  Test: {X_test.shape[0]} samples")

#  NEW architecture (multi-step):
#    ENCODER: LSTM(128) → LSTM(64)  → context vector
#    BRIDGE:  RepeatVector(6)        → repeat for each forecast hour
#    DECODER: LSTM(64) → LSTM(32)   → TimeDistributed Dense
#    Output shape: (batch, 6, 3)    ← next 6 hours × 3 targets
# ════════════════════════════════════════════════════════════
inp     = Input(shape=(SEQ_LEN, len(FEATURES)), name="encoder_input")

# Encoder — reads 24h history and compresses to context vector
enc1    = LSTM(128, return_sequences=True,  name="encoder_lstm1")(inp)
enc1    = Dropout(0.2)(enc1)
enc_out = LSTM(64,  return_sequences=False, name="encoder_lstm2")(enc1)
enc_out = Dropout(0.2)(enc_out)

# Bridge — copy context vector once per forecast step
repeated = RepeatVector(PRED_HORIZON, name="repeat_context")(enc_out)

# Decoder — unrolls over 6 future hours
dec1    = LSTM(64, return_sequences=True, name="decoder_lstm1")(repeated)
dec1    = Dropout(0.2)(dec1)
dec2    = LSTM(32, return_sequences=True, name="decoder_lstm2")(dec1)

# Output — 3 target values per hour, for 6 hours
out     = TimeDistributed(Dense(16, activation="relu"),            name="td_hidden")(dec2)
out     = TimeDistributed(Dense(len(TARGETS), activation="sigmoid"), name="td_output")(out)
# Final shape: (batch, 6, 3)

model = Model(inputs=inp, outputs=out, name="MultiStep_EncoderDecoder_LSTM")
model.compile(
    optimizer=tf.keras.optimizers.Adam(learning_rate=0.001),
    loss="mse",
    metrics=["mae"]
)
model.summary()

# ─── TRAINING ────────────────────────────────────────────
history = model.fit(
    X_train, y_train,
    epochs=30,
    batch_size=64,
    validation_split=0.1,
    callbacks=[
        tf.keras.callbacks.EarlyStopping(
            patience=7, restore_best_weights=True, monitor="val_loss"
        ),
        tf.keras.callbacks.ReduceLROnPlateau(
            factor=0.5, patience=3, min_lr=1e-5, verbose=1
        )
    ],
    verbose=1
)

