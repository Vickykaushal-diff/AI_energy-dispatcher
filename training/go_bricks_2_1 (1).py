    SOLAR_SHARE = 0.55
    WIND_SHARE  = 0.45

    X   = scaler_X.transform(last_24h_df[FEATURES])   # scale
    X   = X.reshape(1, SEQ_LEN, len(FEATURES))         # add batch dim
