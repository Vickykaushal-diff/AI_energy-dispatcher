# ─── LOAD DATA ───────────────────────────────────────────
df = pd.read_csv("weather_labeled.csv").dropna().reset_index(drop=True)

FEATURES = ["temperature", "humidity", "wind_speed",
            "cloud_cover", "precipitation", "pressure", "solar_rad"]
TARGETS  = ["storm_label", "solar_drop_pct", "wind_drop_pct"]

SEQ_LEN  = 24   # 24 ghante ka history
N_SPLITS = 5    # 5 folds

# ─── SEQUENCE BUILDER ────────────────────────────────────
def make_sequences(X, y, seq_len):
    Xs, ys = [], []
    for i in range(len(X) - seq_len):
        Xs.append(X[i:i+seq_len])
        ys.append(y[i+seq_len])
    return np.array(Xs), np.array(ys)

# ─── MODEL BUILDER ───────────────────────────────────────
def build_model(seq_len, n_features, n_targets):
    model = Sequential([
        LSTM(64, return_sequences=True,
             input_shape=(seq_len, n_features)),
        Dropout(0.2),
        LSTM(32, return_sequences=False),
        Dropout(0.2),
        Dense(16, activation='relu'),
        Dense(n_targets, activation='sigmoid')
    ])
    model.compile(optimizer='adam', loss='mse', metrics=['mae'])
    return model

# ─── WALK-FORWARD CV ─────────────────────────────────────
print("=" * 55)
print("   WALK-FORWARD CROSS VALIDATION — WEATHER LSTM")
print("=" * 55)

# Results store karne ke liye
fold_results = []

# Data ko N_SPLITS + 1 equal parts mein baanto
total_len  = len(df)
fold_size  = total_len // (N_SPLITS + 1)

for fold in range(N_SPLITS):
    print(f"\n--- Fold {fold+1}/{N_SPLITS} ---")

    # Train = sab pehle ka data
    train_end  = fold_size * (fold + 1)
    test_start = train_end
    test_end   = test_start + fold_size

    df_train = df.iloc[:train_end]
    df_test  = df.iloc[test_start:test_end]

    print(f"  Train size: {len(df_train)} rows "
          f"({df_train['timestamp'].iloc[0][:10]} → "
          f"{df_train['timestamp'].iloc[-1][:10]})")
    print(f"  Test size : {len(df_test)} rows "
          f"({df_test['timestamp'].iloc[0][:10]} → "
          f"{df_test['timestamp'].iloc[-1][:10]})")

    # Har fold mein fresh scaler fit karo (data leakage avoid)
    scaler_X = MinMaxScaler()
    scaler_y = MinMaxScaler()

    X_train_sc = scaler_X.fit_transform(df_train[FEATURES])
    y_train_sc = scaler_y.fit_transform(df_train[TARGETS])

    # Test pe sirf transform — fit nahi (important!)
    X_test_sc  = scaler_X.transform(df_test[FEATURES])
    y_test_sc  = scaler_y.transform(df_test[TARGETS])

    # Sequences banao
    X_tr, y_tr = make_sequences(X_train_sc, y_train_sc, SEQ_LEN)
    X_te, y_te = make_sequences(X_test_sc,  y_test_sc,  SEQ_LEN)

    if len(X_tr) < 100 or len(X_te) < 10:
        print("  Fold too small, skipping...")
        continue

    # Model train karo
    model = build_model(SEQ_LEN, len(FEATURES), len(TARGETS))
    model.fit(
        X_tr, y_tr,
        epochs=20,
        batch_size=32,
        validation_split=0.1,
        callbacks=[EarlyStopping(patience=3,
                                 restore_best_weights=True)],
        verbose=0   # Silent training
    )

    # Predict karo
    y_pred_sc = model.predict(X_te, verbose=0)

    # Inverse transform — original scale pe laao
    y_pred = scaler_y.inverse_transform(y_pred_sc)
    y_true = scaler_y.inverse_transform(y_te)

    # ─── METRICS ─────────────────────────────────────────
    # Storm detection (classification metrics)
    storm_true = (y_true[:, 0] > 0.5).astype(int)
    storm_pred = (y_pred[:, 0] > 0.5).astype(int)
    storm_acc  = accuracy_score(storm_true, storm_pred)
    storm_f1   = f1_score(storm_true, storm_pred, zero_division=0)

    # Solar drop (regression metrics)
    solar_mae  = mean_absolute_error(y_true[:, 1], y_pred[:, 1])
    solar_rmse = np.sqrt(mean_squared_error(y_true[:, 1], y_pred[:, 1]))

    # Wind drop (regression metrics)
    wind_mae   = mean_absolute_error(y_true[:, 2], y_pred[:, 2])
    wind_rmse  = np.sqrt(mean_squared_error(y_true[:, 2], y_pred[:, 2]))

    result = {
        "fold"       : fold + 1,
        "storm_acc"  : round(storm_acc  * 100, 1),
        "storm_f1"   : round(storm_f1,          3),
        "solar_mae"  : round(solar_mae,          2),
        "solar_rmse" : round(solar_rmse,         2),
        "wind_mae"   : round(wind_mae,           2),
        "wind_rmse"  : round(wind_rmse,          2),
    }
    fold_results.append(result)

    print(f"  Storm Accuracy : {result['storm_acc']}%")
    print(f"  Storm F1 Score : {result['storm_f1']}")
    print(f"  Solar drop MAE : {result['solar_mae']}%")
    print(f"  Wind  drop MAE : {result['wind_mae']}%")

    # Memory clear karo — naya fold ke liye
    tf.keras.backend.clear_session()

