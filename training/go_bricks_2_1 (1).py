    inv = scaler_y.inverse_transform(raw)              # (6, 3) original units

    rows, storm_eta = [], None
    print("\n" + "=" * 60)
    print("  MULTI-STEP FORECAST — Next 6 Hours")
    print("=" * 60)
    print(f"  {'Hour':<6} {'Storm%':<10} {'Solar drop':<14} "
          f"{'Wind drop':<12} {'Deficit MW'}")
    print("  " + "-" * 58)

    for h in range(PRED_HORIZON):
        sp      = float(np.clip(inv[h, 0], 0, 1))
        sd      = float(np.clip(inv[h, 1], 0, 100))
        wd      = float(np.clip(inv[h, 2], 0, 100))
        deficit = round(
            normal_green_mw * SOLAR_SHARE * (sd / 100) +
            normal_green_mw * WIND_SHARE  * (wd / 100), 1
        )
        is_storm = sp >= 0.60
        if is_storm and storm_eta is None:
            storm_eta = h + 1
        bar = "█" * int(sp * 15) + "░" * (15 - int(sp * 15))
        tag = "  ← STORM!" if is_storm else ""
        print(f"  +{h+1}h   [{bar}] {sp*100:>4.0f}%   "
              f"{sd:>5.1f}%         {wd:>5.1f}%    {deficit:>6.1f} MW{tag}")
        rows.append(dict(hour=h+1, storm_prob=round(sp*100,1),
                         solar_drop_pct=round(sd,1),
                         wind_drop_pct=round(wd,1),
                         deficit_mw=deficit, is_storm=is_storm))

    print("=" * 60)
    if storm_eta:
        print(f"\n  ALERT  : Storm in +{storm_eta}h  ({storm_eta*60} minutes)")
        print(f"  Window : {storm_eta*60 - 15} min before impact — act now!")
    else:
        print("\n  OK — No storm expected in next 6 hours")

    return pd.DataFrame(rows), storm_eta


